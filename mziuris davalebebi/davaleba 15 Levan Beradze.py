class Cat:
    def eat(self):
        print("Cat eats milk")

    def talk(self):
        print("Cat says miaww")

    def walk(self):
        print("Cat can run 20km/h")

class Dog:
    def eat(self):
        print("Dog eats a bone")

    def talk(self):
        print("Dog says Aww")

    def walk(self):
        print("Dog can run 40km/h")


cat = Cat()
cat.eat()
cat.talk()
cat.walk()
dog = Dog()
dog.eat()
dog.talk()
dog.walk()
